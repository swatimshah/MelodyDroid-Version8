package com.droid.melodydroid.display;

public class SongItem {
	private String songName;
	private String songAbsolutePath;
	private Long songId;

	public SongItem(String mySongName, String mySongAbsolutePath, Long mySongId) {
		this.songName = mySongName;
		this.songAbsolutePath = mySongAbsolutePath;
		this.songId = mySongId;
	}

	public Long getSongId() {
		return songId;
	}
	
	public void setSongId(Long songId) {
		this.songId = songId;
	}
	
	public String getSongName() {
		return songName;
	}
	public void setSongName(String songName) {
		this.songName = songName;
	}
	public String getSongAbsolutePath() {
		return songAbsolutePath;
	}
	public void setSongAbsolutePath(String songAbsolutePath) {
		this.songAbsolutePath = songAbsolutePath;
	}	
}
